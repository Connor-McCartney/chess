void update_board(piece_t *board[8][8]) {
    for (int i=0; i<8; i++) {
        for (int j=0; j<8; j++) {
            mvwaddstr(stdscr, 15 - 2*j, 6 + 4*i, board[i][j]->unicode_char);
        }
    }
    refresh();
}

void run(void) {
    curs_set(0); // hide cursor
    draw_border();
    piece_t *board[8][8];
    initalise_board(board);
    update_board(board);

    getch();
}


